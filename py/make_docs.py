def build(out: Path) -> list[Path]:
    missing = check_coverage()
    if missing:
        raise SystemExit(
            "Undocumented terms ŌĆö add them to py/ips_docs_text.py:\n  "
            + "\n  ".join(missing))
    out.mkdir(parents=True, exist_ok=True)
    return [page_index(out), page_model(out), page_vocabulary(out),
            page_crosswalk(out), page_statistics(out), page_queries(out),
            page_bundle(out), page_open(out)]


def main() -> int:
    ap = argparse.ArgumentParser(description="Generate docs/*.md")
    ap.add_argument("--out", type=Path, default=ROOT / "docs")
    args = ap.parse_args()
    for p in build(args.out):
        print("  ", p.relative_to(ROOT))
    return 0


if __name__ == "__main__":
    sys.exit(main())
