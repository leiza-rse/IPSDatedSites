def local(term) -> str:
    """Local name of a URIRef."""
    s = str(term)
    return s.split("#")[-1] if "#" in s else s.rsplit("/", 1)[-1]


def qname(term) -> str:
    """Prefixed name, using the export's own prefix map."""
    s = str(term)
    for pfx, ns in X.PREFIXES.items():
        if s.startswith(str(ns)):
            return f"{pfx}:{s[len(str(ns)):]}"
    return f"<{s}>"


def doc(term) -> str:
    return T.TERM_DOCS.get(local(term), "")


def check_coverage() -> list[str]:
    """Every term in the code must be documented."""
    missing = []
    for cls, *_ in X.CLASSES:
        if local(cls) not in T.TERM_DOCS:
            missing.append(f"class {local(cls)}")
    for prop, *_ in X.OBJ_PROPS + X.DATA_PROPS:
        if local(prop) not in T.TERM_DOCS:
            missing.append(f"property {local(prop)}")
    for pfx in X.PREFIXES:
        if pfx not in T.CROSSWALK_NOTES:
            missing.append(f"namespace {pfx}")
    return missing


HEADER = ("<!-- Generated by py/make_docs.py ŌĆö do not edit by hand.\n"
          "     The structure is read from the code; the prose lives in\n"
          "     py/ips_docs_text.py. Edit those, then re-run the pipeline. -->\n")


def write(out: Path, name: str, body: str) -> Path:
    p = out / name
    p.write_text(HEADER + "\n" + body.rstrip() + "\n", encoding="utf-8")
    return p


